package Pattern;

public class Pattern9 {
    public static void main(String[] args) {
    	
    		int rows=3,cols=3,count=0;                   //123
    		                                     //456
    		                                     //789
    		for(int r=1;r<=rows;r++)
    		{
    			for(int c=1;c<=cols;c++)
    			{
    				count++;
    				System.out.print(count+" ");
    				
    			}
    			System.out.println();
    	}
    	}
	}

