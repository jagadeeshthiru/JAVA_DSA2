package Patternmodel2;

public class Pattern5 {
	public static void main(String[] args) 
    {
	    int rows=5,cols=5;
	 
	    for(int r=1;r<=rows;r++)
	    { 
	    	char letter='A';
	    	for(int c=1;c<=cols;c++)
	    	{
	    		
	    	System.out.print(letter+" ");	
	    	letter++;
	    	}
	    
	    	
	    	System.out.println();
	    }
     }
}
